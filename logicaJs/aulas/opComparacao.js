/* 
    <== Operadores de Comparação==>

    • > -> Maior Que
    • >= -> Maior Que ou Igual a
    • < -> Menor Que
    • <= -> Menor Que ou Igual a
    • == -> Igualdade (Apenas Valores) -> Não Recomendado Usar
    • === -> Igualdade Estrita (Valores e Tipos)
    • != -> Diferente (Apenas Valores) -> Não Recomendado Usar
    • !== -> Diferente Estrito (Valores e Tipos)
*/

const num1 = 10 // Declara a variável e seu valor
const num2 = 11 // Declara a variável e seu valor
const comparacao = num1 === num2 // Declara a variável e compara os valores de num1 e num2

console.log(comparacao) // Exibe no console o resultado boolean (true or false)
